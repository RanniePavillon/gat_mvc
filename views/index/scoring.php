	<!-- C -->
	<div class="row">
		<h3><strong> C = Control </strong></h3>
		<p>
			<em>To what extent can you influence the situation? </em>
			<br>
			<br>
			<em>How much control do you perceive you have?  </em>
		</p>
		<p>
			Those with higher AQs perceive they have significantly more control and influence 
			in adverse situations than do those with lower AQs.  Even in situations that appear 
			overwhelming or out of their hands, those with higher AQs find some facet of the 
			situation they can influence.  Those with lower AQs respond as if they have little 
			or no control and often give up.  
		</p>
	</div>
	
	<!-- O -->
	<div class="row">
		<h3><strong> O = Ownership  </strong></h3>
		<p>
			<em>To what extent do you hold yourself responsible for improving this situation?  </em>
			<br>
			<br>
			<em>To what extent are you accountable to play some role in making it better?   </em>
		</p>
		<p>
			Accountability is the backbone of action.  Those with higher AQs hold themselves 
			accountable for dealing with situations regardless of their cause.  Those with 
			lower AQs deflect accountability and most often feel victimized and helpless.  
		</p>
	</div>
	
	<!-- R -->
	<div class="row">
		<h3><strong> R = Reach  </strong></h3>
		<p>
			<em>How far does the fallout of this situation reach into other areas of your work or life?  </em>
			<br>
			<br>
			<em>To what extent does the adversity extend beyond the situation at hand?   </em>
		</p>
		<p>
			Keeping the fallout under control and limiting the reach of adversity is essential 
			for efficient and effective problem solving.  Those with higher AQs keep setbacks 
			and challenges in their place, not letting them infest the healthy areas of their 
			work and lives.  Those with lower AQs tend to catastrophize, allowing a setback 
			in one area to bleed into other, unrelated areas and become destructive.  
		</p>
	</div>
	
	<!-- E -->
	<div class="row">
		<h3><strong> E = Endurance  </strong></h3>
		<p>
			<em>How long will the adversity endure? </em>
		</p>
		<p>
			Seeing beyond even enormous difficulties is an essential skill for maintaining hope. 
			Those with higher AQs have the uncanny ability to see past the most interminable 
			difficulties and maintain hope and optimism.  Those with lower AQs see adversity as
			dragging on indefinitely, if not permanently.  
		</p>
	</div>
